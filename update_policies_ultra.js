async function upsertPage(page) {
  // Delete existing
  await fetch(`${SUPABASE_URL}/rest/v1/pages?slug=eq.${page.slug}`, {
    method: 'DELETE',
    headers: {
      'apikey': SERVICE_KEY,
      'Authorization': `Bearer ${SERVICE_KEY}`,
    }
  });

  // Insert fresh
  const res = await fetch(`${SUPABASE_URL}/rest/v1/pages`, {
    method: 'POST',
    headers: {
      'apikey': SERVICE_KEY,
      'Authorization': `Bearer ${SERVICE_KEY}`,
      'Content-Type': 'application/json',
      'Prefer': 'return=representation'
    },
    body: JSON.stringify({
      slug: page.slug,
      title: page.title,
      content: page.content,
    })
  });

  if (res.ok) {
    console.log(`✅ ${page.slug} — updated to ULTRA detailed version`);
  } else {
    const data = await res.json();
    console.error(`❌ ${page.slug} — ERROR:`, JSON.stringify(data));
  }
}

